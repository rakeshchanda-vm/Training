def supervisor_task_routing_prompt(
    agents: Dict,
    tasks: list,
    today: str,
    extended_prompt: str,
    plan: List | str,
    current_step: str,
    validation_attempts: int,
    max_validation_attempts: int,
    step_validation_status: bool,
    completed_plan_steps: int,
    total_plan_steps: int,
    report_structure: str,
):
    sections = []
    sections.append(
        f"You are an orchestrator (supervisor) responsible for coordinating the following workers: {list(agents.keys())}."
    )
    sections.append(
        f"Available workers & their tools: {', '.join(list(set([f'{agent.name}: {agent.tools}' for agent in agents.values()])))}"
    )
    if len(tasks) > 3:
        sections.append(
            "The provided chat history contains actions taken by you and the workers."
        )
    sections.append(f"Today's date: {today}")
    sections.append("Your responsibilities include:")
    sections.append(
        "1. Reviewing the current state of the conversation history and determining the next best worker to handle a task."
    )
    sections.append(
        "2. Breaking down complex requirements into clear, actionable tasks suitable for the available workers."
    )
    sections.append(
        "3. Providing all necessary context in each task assignment, as workers do not have access to previous conversation history."
    )
    sections.append(
        "4. Monitoring progress and determining when the user's request is complete."
    )
    sections.append(
        "5. Ensuring each task meaningfully contributes to the overall requirement or goals."
    )
    sections.append(
        "6. Avoiding repetition: Do not assign the same or very similar tasks to the same or different workers. If needed, create new tasks that add value or cover new ground."
    )
    sections.append("Guidelines:")
    sections.append("- Do not repeat tasks to the same or different worker.")
    sections.append("- Provide complete and clear context in each task assignment.")
    sections.append(
        "- Ensure the overall project or workflow is progressing toward completion."
    )
    sections.append(
        "- Ensure simple user messages such as greetings go straight to 'FINISH' step, or simple requirements can be done with little iterations."
    )
    sections.append(
        "- If you or the workers are unable to come up with required information or an answer, go to FINISH."
    )
    if extended_prompt:
        sections.append(f"Additional Instructions:\n{extended_prompt}")
    if plan:
        if current_step:
            sections.append(f"CURRENT PLAN STEP TO FOCUS ON: {current_step}")
            if validation_attempts > 0 and not step_validation_status:
                sections.append(
                    f"This step has been worked on but may require more attention ({validation_attempts}/{max_validation_attempts} attempts). Please provide a more comprehensive or creative task to ensure this step is completed thoroughly."
                )
            sections.append(
                "Focus ONLY on the CURRENT PLAN STEP shown above. Do not move to other steps until this one is complete."
            )
            sections.append(
                "Create detailed, specific tasks that will thoroughly address all aspects of the current step."
            )
        sections.append(
            f"Plan progress: {completed_plan_steps}/{total_plan_steps} steps worked on"
        )
    if report_structure:
        sections.append(
            f"Expected Final Output Structure (for reference when assigning tasks):\n<start>\n\n{report_structure}\n\n<end>"
        )
    sections.append("Response format:")
    sections.append(
        "- Return JSON with 'next' (worker name) and 'message' (specific instructions)."
    )
    sections.append(
        "- The 'message' should be either a response to the user or a task to the agent, detailed, specific, and aligned with the current state."
    )
    sections.append(
        "- Include 'additional_context' to help the worker understand the task better."
    )
    return "\n".join(sections)


def _format_agent_roster(agents: Dict) -> str:
    if not agents:
        return "No specialist agents are configured."

    lines = []
    for name, agent in agents.items():
        tools = getattr(agent, "tools", []) or []
        tool_names = [
            getattr(tool, "name", None) or getattr(tool, "__name__", None) or str(tool)
            for tool in tools
        ]
        tools_text = ", ".join(tool_names) if tool_names else "no tools"
        lines.append(f"- {name}: tools={tools_text}")
    return "\n".join(lines)


def _coordination_tool_instructions(
    *,
    enable_planning: bool = False,
    allow_parallel_agents: bool = False,
    allow_async_tasks: bool = False,
) -> str:
    instructions = []
    if enable_planning:
        instructions.append(
            "- Planning tools are available. For multi-step, ambiguous, or stateful work, use the todo/planning tools early, keep the plan current, and mark steps complete only after they are actually done."
        )
    if allow_parallel_agents:
        instructions.append(
            "- The parallel_task tool is available. Use it for independent sub tasks that can safely run at the same time, then synthesize the returned results before deciding what to do next."
        )
    if allow_async_tasks:
        instructions.append(
            "- Async task tools are available. Use async_task for independent long-running agent work when you can continue coordinating or inspecting other work while it runs. Before relying on results or finishing, use get_task_status, wait, or cancel_task so no needed background work is left unresolved."
        )
    if not instructions:
        return "- No optional planning, parallel, or async coordination tool family is enabled. Coordinate with the available routing tools."
    return "\n".join(instructions)


