            f"The current task type is '{task_type}'. "
            "Based on the task at hand, generate a plan of action to achieve the task using the available agents.\n"
            "Create a high-level plan with clear objectives rather than assuming specific details, especially for ambiguous research tasks.\n\n"
            "Focus on identifying the key information categories to explore rather than speculating on specific findings.\n"
            "For example, identify broad areas to investigate, general approaches to validation, and types of information to gather.\n"
